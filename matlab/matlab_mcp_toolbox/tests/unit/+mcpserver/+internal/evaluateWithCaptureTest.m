        function [mockController, controllerBehavior] = createMockVisibilityController(testCase, showFigureWindows)
            import matlab.mock.actions.DoNothing

            [mockController, controllerBehavior] = testCase.createMock( ...
                ?mcpserver.internal.display.FigureVisibilityController, ...
                Strict=true ...
            );

            if showFigureWindows
                when(controllerBehavior.show().withExactInputs(), DoNothing);
            else
                when(controllerBehavior.hide().withExactInputs(), DoNothing);
            end
            when(controllerBehavior.restore().withExactInputs(), DoNothing);
        end

