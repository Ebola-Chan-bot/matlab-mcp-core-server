        function testDefaultWindowsACLManager_GetAllowedSIDs_Directory_ReturnsAllowSIDsOnly(testCase)
            % Arrange
            import matlab.mock.actions.AssignOutputs
            import matlab.mock.actions.DoNothing

            [mockFacade, facadeBehavior] = testCase.createMock( ...
                ?mcpserver.internal.facade.dotnet.DotNetFacade, ...
                Strict=true ...
            );
            [mockAccessControl, ~] = testCase.createMock( ...
                ?mcpserver.internal.fs.internal.permissionmanager.internal.windowsacl.internal.dotnet.DotNetAccessControl, ...
                Strict=true ...
            );

            tempFixture = testCase.applyFixture(matlab.unittest.fixtures.TemporaryFolderFixture);
            testFolder = tempFixture.Folder;

            when( ...
                facadeBehavior.getAccessSectionsAccess().withExactInputs(), ...
                AssignOutputs("AccessSections.Access") ...
            );
            when( ...
                facadeBehavior.getAllowAccessControlType().withExactInputs(), ...
                AssignOutputs("Allow") ...
            );

            mockIdentity = testCase.createMockIdentity(testCase.TestUserSID);
            when( ...
                facadeBehavior.getCurrentWindowsIdentity().withExactInputs(), ...
                AssignOutputs(mockIdentity) ...
            );

            systemSID = testCase.SystemSID;
            adminsSID = testCase.AdministratorsSID;
            authUsersSID = testCase.AuthenticatedUsersSID;
            rules = testCase.createMockRules([
                struct(AccessControlType="Allow", IdentityReference=struct(ToString=@() systemSID)), ...
                struct(AccessControlType="Deny", IdentityReference=struct(ToString=@() authUsersSID)), ...
                struct(AccessControlType="Allow", IdentityReference=struct(ToString=@() adminsSID))
            ]);
            mockSecurity = testCase.createMockSecurity(rules);
            when( ...
                facadeBehavior.DirectorySecurity(testFolder, "AccessSections.Access"), ...
                AssignOutputs(mockSecurity) ...
            );

            manager = mcpserver.internal.fs.internal.permissionmanager.internal.windowsacl.DefaultWindowsACLManager( ...
                DotNetAccessControl=mockAccessControl, ...
                DotNetFacade=mockFacade ...
            );

            % Act
            sids = manager.getAllowedSIDs(testFolder);

            % Assert
            testCase.verifyEqual(sids, [testCase.SystemSID, testCase.AdministratorsSID]);
        end

