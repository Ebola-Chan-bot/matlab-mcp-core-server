        function rules = createMockRules(~, ruleStructs)
            rules = struct( ...
                Count=numel(ruleStructs), ...
                Item=@(i) ruleStructs(i+1) ...
            );
        end

        function security = createMockSecurity(~, rules)
            security = struct( ...
                GetAccessRules=@(~,~,~) rules ...
            );
        end

        function security = createMockSecurityWithSddl(~, sddlString)
            security = struct( ...
                GetSecurityDescriptorSddlForm=@(~) sddlString ...
            );
        end
