classdef DefaultSecureGeneratorTest < matlab.mock.TestCase
    %DefaultSecureGeneratorTest Tests for DefaultSecureGenerator
    %   This test class verifies that DefaultSecureGenerator correctly
    %   generates cryptographically secure keys using OS-level CSPRNGs.
    %   All system calls are mocked via facades.

    % Copyright 2026 The MathWorks, Inc.

    properties (Constant, Access = private)
        ExpectedKeySize(1,1) uint8 = 24
    end

    methods (Test)
        function testDefaultSecureGenerator_Constructor_HappyPath(testCase)
            % Arrange
            [mockOSFacade, ~] = testCase.createMock( ...
                ?mcpserver.internal.facade.os.OSFacade, ...
                Strict=true ...
            );
            [mockFSFacade, ~] = testCase.createMock( ...
                ?mcpserver.internal.facade.fs.FSFacade, ...
                Strict=true ...
            );
            [mockDotNetFacade, ~] = testCase.createMock( ...
                ?mcpserver.internal.facade.dotnet.DotNetFacade, ...
                Strict=true ...
            );

            % Act
            generator = mcpserver.internal.connector.internal.apikeymanager.internal.securegen.DefaultSecureGenerator( ...
                OSFacade=mockOSFacade, ...
                FSFacade=mockFSFacade, ...
                DotNetFacade=mockDotNetFacade ...
            );

            % Assert
            testCase.verifyInstanceOf( ...
                generator, ...
                ?mcpserver.internal.connector.internal.apikeymanager.internal.securegen.SecureGenerator, ...
                "DefaultSecureGenerator should be a SecureGenerator" ...
            );
        end

        function testDefaultSecureGenerator_GenerateKey_MacOS_ReadsFromDevUrandom(testCase)
            % Arrange
            import matlab.mock.actions.AssignOutputs
            import matlab.mock.actions.DoNothing

            [mockOSFacade, osBehavior] = testCase.createMock( ...
                ?mcpserver.internal.facade.os.OSFacade, ...
                Strict=true ...
            );
            [mockFSFacade, fsBehavior] = testCase.createMock( ...
                ?mcpserver.internal.facade.fs.FSFacade, ...
                Strict=true ...
            );

            expectedBytes = uint8(0:23);
            expectedKey = lower(strjoin(string(dec2hex(expectedBytes, 2)), ""));
            fid = 42;

            when( ...
                osBehavior.ispc().withExactInputs(), ...
                AssignOutputs(false) ...
            );
            when( ...
                osBehavior.ismac().withExactInputs(), ...
                AssignOutputs(true) ...
            );
            when( ...
                fsBehavior.fopen(fullfile("/", "dev", "urandom"), "r"), ...
                AssignOutputs(fid) ...
            );
            when( ...
                fsBehavior.fread(fid, testCase.ExpectedKeySize, "*uint8"), ...
                AssignOutputs(expectedBytes) ...
            );
            when( ...
                fsBehavior.fclose(fid), ...
                DoNothing ...
            );

            generator = testCase.createGenerator(mockOSFacade, mockFSFacade);

            % Act
            key = generator.generateKey();

            % Assert
            testCase.verifyEqual(key, expectedKey, ...
                "Key should be hex-encoded bytes from /dev/urandom");
        end

