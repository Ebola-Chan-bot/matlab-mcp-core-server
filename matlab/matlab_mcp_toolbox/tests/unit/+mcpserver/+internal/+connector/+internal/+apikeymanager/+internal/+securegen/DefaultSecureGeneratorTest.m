        function testDefaultSecureGenerator_GenerateKey_Windows_ProducesCorrectHexKey(testCase)
            % Arrange
            import matlab.mock.actions.AssignOutputs
            import matlab.mock.actions.DoNothing

            [mockOSFacade, osBehavior] = testCase.createMock( ...
                ?mcpserver.internal.facade.os.OSFacade, ...
                Strict=true ...
            );
            [mockFSFacade, ~] = testCase.createMock( ...
                ?mcpserver.internal.facade.fs.FSFacade, ...
                Strict=true ...
            );
            [mockDotNetFacade, dotNetBehavior] = testCase.createMock( ...
                ?mcpserver.internal.facade.dotnet.DotNetFacade, ...
                Strict=true ...
            );

            when( ...
                osBehavior.ispc().withExactInputs(), ...
                AssignOutputs(true) ...
            );

            expectedBytes = uint8(0:23);
            expectedKey = lower(strjoin(string(dec2hex(expectedBytes, 2)), ""));
            mockRng = struct( ...
                Dispose=@() [], ...
                GetBytes=@(~) [] ...
            );

            when( ...
                dotNetBehavior.createRandomNumberGenerator().withExactInputs(), ...
                AssignOutputs(mockRng) ...
            );
            when( ...
                dotNetBehavior.createByteArray(testCase.ExpectedKeySize), ...
                AssignOutputs(expectedBytes) ...
            );

            generator = mcpserver.internal.connector.internal.apikeymanager.internal.securegen.DefaultSecureGenerator( ...
                OSFacade=mockOSFacade, ...
                FSFacade=mockFSFacade, ...
                DotNetFacade=mockDotNetFacade ...
            );

            % Act
            key = generator.generateKey();

            % Assert
            testCase.verifyEqual(key, expectedKey, ...
                "Key should be hex-encoded bytes from .NET RNG");
        end

