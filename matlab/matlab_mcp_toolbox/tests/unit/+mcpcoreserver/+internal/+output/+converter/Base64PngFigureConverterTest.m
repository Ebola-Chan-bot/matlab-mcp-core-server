        function testBase64PngFigureConverter_Convert_DeleteThrows_SwallowedSilently(testCase)
            import matlab.mock.actions.AssignOutputs
            import matlab.mock.actions.DoNothing
            import matlab.mock.actions.ThrowException

            mocks = testCase.createDefaultMocks();
            expectedFiguresFolder = fullfile(testCase.FakeAppDataFolder, "v1", "figures");
            expectedTmpPath = fullfile(expectedFiguresFolder, "fakepath");
            expectedB64 = "iVBORw0KGg==";
            fakeFileId = 42;

            when( ...
                mocks.appDataBehavior.getAppDataFolder().withExactInputs(), ...
                AssignOutputs(testCase.FakeAppDataFolder) ...
            );
            when( ...
                mocks.fsAdaptorBehavior.ensureSecureFolder(expectedFiguresFolder), ...
                DoNothing ...
            );
            when( ...
                mocks.exportBehavior.tempname(expectedFiguresFolder), ...
                AssignOutputs(expectedTmpPath) ...
            );
            when( ...
                withAnyInputs(mocks.exportBehavior.exportgraphics), ...
                DoNothing ...
            );
            when( ...
                withAnyInputs(mocks.exportBehavior.base64encode), ...
                AssignOutputs(expectedB64) ...
            );
            when( ...
                withAnyInputs(mocks.fsBehavior.fopen), ...
                AssignOutputs(fakeFileId) ...
            );
            when( ...
                withAnyInputs(mocks.fsBehavior.fread), ...
                AssignOutputs(uint8([1 2 3])) ...
            );
            when( ...
                withAnyInputs(mocks.fsBehavior.fclose), ...
                DoNothing ...
            );
            when( ...
                withAnyInputs(mocks.fsBehavior.delete), ...
                ThrowException(MException('MATLAB:DELETE:FileNotFound', 'file not found')) ...
            );

            converter = testCase.createConverter(mocks);
            evt = struct('type', 'figure', 'payload', 0);

            result = converter.convert(evt);

            testCase.verifyCalled( ...
                mocks.fsBehavior.delete(expectedTmpPath + ".png"), ...
                "delete should be attempted unconditionally" ...
            );
            testCase.verifyEqual(result.data, char(expectedB64), ...
                "convert should still return the encoded payload when delete throws");
        end
    end

    methods (Static, Access = private)
        function converter = createConverter(mocks, options)
            arguments
                mocks
                options.Resolution = []
            end
            args = { ...
                'AppDataLocator', mocks.mockAppData, ...
                'FSAdaptor',      mocks.mockFSAdaptor, ...
                'ExportFacade',   mocks.mockExport, ...
                'FSFacade',       mocks.mockFS ...
            };
            if ~isempty(options.Resolution)
                args = [args, {'Resolution', options.Resolution}];
            end
            converter = mcpcoreserver.internal.output.converter.Base64PngFigureConverter(args{:});
        end
    end

