                mocks.fsAdaptorBehavior.ensureSecureFolder(expectedFiguresFolder), ...
                DoNothing ...
            );
            when( ...
                mocks.exportBehavior.tempname(expectedFiguresFolder), ...
                AssignOutputs(expectedTmpPath) ...
