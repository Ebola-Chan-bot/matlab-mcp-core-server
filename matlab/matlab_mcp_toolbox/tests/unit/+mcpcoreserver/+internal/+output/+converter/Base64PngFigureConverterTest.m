    methods (Test)
        function testBase64PngFigureConverter_Convert_HappyPath_ReturnsBase64Struct(testCase)
            import matlab.mock.actions.AssignOutputs
            import matlab.mock.actions.DoNothing

            mocks = testCase.createDefaultMocks();
            expectedFiguresFolder = fullfile(testCase.FakeAppDataFolder, "v1", "figures");
            expectedTmpPath = fullfile(expectedFiguresFolder, "fakepath");
            expectedBytes = uint8([137 80 78 71]);
            expectedB64 = "iVBORw0KGg==";
            fakeFileId = 42;

            when( ...
                mocks.appDataBehavior.getAppDataFolder().withExactInputs(), ...
                AssignOutputs(testCase.FakeAppDataFolder) ...
            );
            when( ...
                mocks.fsAdaptorBehavior.ensureSecureFolder(expectedFiguresFolder), ...
                DoNothing ...
            );
            when( ...
                mocks.exportBehavior.tempname(expectedFiguresFolder), ...
                AssignOutputs(expectedTmpPath) ...
            );
            when( ...
                withAnyInputs(mocks.exportBehavior.exportgraphics), ...
                DoNothing ...
            );
            when( ...
                mocks.exportBehavior.base64encode(expectedBytes), ...
                AssignOutputs(expectedB64) ...
            );
            when( ...
                mocks.fsBehavior.fopen(expectedTmpPath + ".png", 'r'), ...
                AssignOutputs(fakeFileId) ...
            );
            when( ...
                mocks.fsBehavior.fread(fakeFileId, Inf, '*uint8'), ...
                AssignOutputs(expectedBytes) ...
            );
            when( ...
                withAnyInputs(mocks.fsBehavior.fclose), ...
                DoNothing ...
            );
            when( ...
                withAnyInputs(mocks.fsBehavior.delete), ...
                DoNothing ...
            );

            converter = testCase.createConverter(mocks);
            evt = struct('type', 'figure', 'payload', 0);

            result = converter.convert(evt);

            testCase.verifyEqual(result.outputType, 'figure');
            testCase.verifyEqual(result.mimeType, 'image/png');
            testCase.verifyEqual(result.data, char(expectedB64));
        end

